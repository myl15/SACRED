class InterventionHook:
    """
    Manages intervention hooks for ablation and patching.
    Properly handles hook lifecycle to avoid stacking.
    """

    def __init__(self):
        self.handles = []
        self.intervention_type = None
        self.intervention_params = {}

    def register_ablation_hook(
        self,
        model,
        circuit: Circuit,
        component_type: str = "mlp"
    ):
        """
        Register hook to ablate (zero out) circuit components.

        Args:
            model: NLLB model
            circuit: Circuit object with components to ablate
            component_type: "mlp" or "attn"
        """
        self.cleanup()
        self.intervention_type = "ablation"

        # Group neurons by layer
        neurons_by_layer = defaultdict(list)
        for neuron in circuit.neurons:
            neurons_by_layer[neuron.layer].append(neuron.neuron_idx)

        # Register hooks for each layer
        for layer_idx, neuron_indices in neurons_by_layer.items():
            if component_type == "mlp":
                target_module = model.model.encoder.layers[layer_idx].fc1
            else:
                raise NotImplementedError(f"Component type {component_type} not yet supported")

            handle = target_module.register_forward_hook(
                self._make_ablation_hook(neuron_indices)
            )
            self.handles.append(handle)

        self.intervention_params["neurons_by_layer"] = neurons_by_layer

    def _make_ablation_hook(self, neuron_indices: List[int]) -> Callable:
        """Create ablation hook function."""
        def hook_fn(module, input, output):
            # Zero out specified neurons
            output_copy = output.clone()
            output_copy[:, :, neuron_indices] = 0.0
            return output_copy
        return hook_fn

    def register_random_ablation_hook(
        self,
        model,
        n_neurons_to_ablate: int,
        layers: List[int],
        intermediate_dim: int = 8192
    ):
        """
        Register hook to ablate random neurons (negative control).

        Args:
            model: NLLB model
            n_neurons_to_ablate: Number of neurons to randomly ablate per layer
            layers: Layers to ablate
            intermediate_dim: Size of intermediate dimension
        """
        self.cleanup()
        self.intervention_type = "random_ablation"

        for layer_idx in layers:
            # Randomly sample neurons
            random_neurons = np.random.choice(
                intermediate_dim,
                size=n_neurons_to_ablate,
                replace=False
            ).tolist()

            target_module = model.model.encoder.layers[layer_idx].fc1
            handle = target_module.register_forward_hook(
                self._make_ablation_hook(random_neurons)
            )
            self.handles.append(handle)

    def register_patching_hook(
        self,
        model,
        circuit: Circuit,
        clean_activations: Dict[int, torch.Tensor]
    ):
        """
        Register hook to patch in clean activations (for sufficiency testing).

        Args:
            model: NLLB model
            circuit: Circuit to restore
            clean_activations: Dictionary mapping layers to clean activation tensors
        """
        self.cleanup()
        self.intervention_type = "patching"

        neurons_by_layer = defaultdict(list)
        for neuron in circuit.neurons:
            neurons_by_layer[neuron.layer].append(neuron.neuron_idx)

        for layer_idx, neuron_indices in neurons_by_layer.items():
            if layer_idx not in clean_activations:
                continue

            target_module = model.model.encoder.layers[layer_idx].fc1
            handle = target_module.register_forward_hook(
                self._make_patching_hook(neuron_indices, clean_activations[layer_idx])
            )
            self.handles.append(handle)

    def _make_patching_hook(
        self,
        neuron_indices: List[int],
        clean_acts: torch.Tensor
    ) -> Callable:
        """Create patching hook function."""
        def hook_fn(module, input, output):
            output_copy = output.clone()
            # Patch in clean activations for specified neurons
            output_copy[:, :, neuron_indices] = clean_acts[:, :, neuron_indices]
            return output_copy
        return hook_fn

    def cleanup(self):
        """Remove all registered hooks."""
        for handle in self.handles:
            handle.remove()
        self.handles = []


def validate_intervention_execution(
    model,
    circuit: Circuit,
    test_sentence: str,
    tokenizer,
    lang_code: str,
    intervention_hook: InterventionHook,
    device: str = "cuda"
) -> Dict[str, Any]:
    """
    CRITICAL: Verify that intervention actually happened.

    This addresses a major gap in the original notebook.

    Args:
        model: NLLB model
        circuit: Circuit being tested
        test_sentence: Sentence to test with
        tokenizer: NLLB tokenizer
        lang_code: Language code
        intervention_hook: Active intervention hook
        device: Device

    Returns:
        Validation report with pass/fail status
    """
    report = {
        "passed": True,
        "errors": [],
        "neuron_checks": []
    }

    # Capture baseline activations (no intervention)
    #from circuit_discovery import ActivationCapture

    capture = ActivationCapture()
    layers = circuit.get_critical_layers()
    capture.register_hooks(model, layers, component_type="mlp")

    inputs = tokenizer(test_sentence, return_tensors="pt", src_lang=lang_code).to(device)

    # First pass: No intervention
    intervention_hook.cleanup()
    with torch.no_grad():
        # Call encoder directly to skip decoder entirely
        _ = model.model.encoder(
            input_ids=inputs["input_ids"],
            attention_mask=inputs.get("attention_mask")
        )

    baseline_acts = {}
    for layer in layers:
        acts = capture.get_activations(layer, "mlp")
        if acts is not None:
            baseline_acts[layer] = acts.clone()

    # Second pass: With intervention
    capture.activations.clear()

    if intervention_hook.intervention_type == "ablation":
        intervention_hook.register_ablation_hook(model, circuit, "mlp")
    elif intervention_hook.intervention_type == "random_ablation":
        # Restore random ablation
        intervention_hook.register_random_ablation_hook(
            model,
            n_neurons_to_ablate=len(circuit.neurons) // len(layers),
            layers=layers
        )

    with torch.no_grad():
        # Call encoder directly to skip decoder entirely
        _ = model.model.encoder(
            input_ids=inputs["input_ids"],
            attention_mask=inputs.get("attention_mask")
        )

    intervened_acts = {}
    for layer in layers:
        acts = capture.get_activations(layer, "mlp")
        if acts is not None:
            intervened_acts[layer] = acts.clone()

    capture.cleanup()

    # Verify ablation happened
    if intervention_hook.intervention_type in ["ablation", "random_ablation"]:
        neurons_by_layer = defaultdict(list)
        for neuron in circuit.neurons:
            neurons_by_layer[neuron.layer].append(neuron.neuron_idx)

        for layer in layers:
            if layer not in baseline_acts or layer not in intervened_acts:
                continue

            for neuron_idx in neurons_by_layer.get(layer, []):
                baseline_val = baseline_acts[layer][0, 0, neuron_idx].item()
                intervened_val = intervened_acts[layer][0, 0, neuron_idx].item()

                # Check if neuron was zeroed
                if abs(intervened_val) > 1e-5:
                    report["passed"] = False
                    report["errors"].append(
                        f"Layer {layer}, Neuron {neuron_idx}: Expected ~0, got {intervened_val:.6f}"
                    )

                report["neuron_checks"].append({
                    "layer": layer,
                    "neuron": neuron_idx,
                    "baseline": baseline_val,
                    "intervened": intervened_val,
                    "zeroed": abs(intervened_val) < 1e-5
                })

    return report


def measure_translation_quality(
    sentences: List[Dict],
    model,
    tokenizer,
    lang_code: str,
    target_lang: str,
    sacred_token_ids: List[int],
    intervention: Optional[InterventionHook] = None,
    device: str = "cuda"
) -> List[QualityMetrics]:
    """
    Measure translation quality with multiple metrics.

    Args:
        sentences: List of sentence dictionaries
        model: NLLB model
        tokenizer: NLLB tokenizer
        lang_code: Source language code
        target_lang: Target language code
        sacred_token_ids: List of sacred token IDs to check for
        intervention: Optional intervention hook
        device: Device

    Returns:
        List of QualityMetrics for each sentence
    """
    quality_metrics = []

    for sent_dict in sentences:
        sentence = sent_dict["text"]

        inputs = tokenizer(
            sentence,
            return_tensors="pt",
            src_lang=lang_code
        ).to(device)

        with torch.no_grad():
            # Get target language token ID
            target_lang_id = tokenizer.convert_tokens_to_ids(target_lang)

            # Only pass input_ids and attention_mask to generate (not decoder inputs)
            outputs = model.generate(
                input_ids=inputs["input_ids"],
                attention_mask=inputs.get("attention_mask"),
                forced_bos_token_id=target_lang_id,
                max_length=50,
                num_beams=1,
                output_scores=True,
                return_dict_in_generate=True
            )

        generated_ids = outputs.sequences[0]
        translation = tokenizer.decode(generated_ids, skip_special_tokens=True)

        # Check for sacred tokens
        sacred_present = any(token_id in generated_ids.tolist() for token_id in sacred_token_ids)

        # Compute sacred token probability
        if hasattr(outputs, 'scores') and outputs.scores:
            # Average probability of sacred tokens across generation steps
            sacred_probs = []
            for step_scores in outputs.scores:
                probs = F.softmax(step_scores[0], dim=0)
                sacred_prob = probs[sacred_token_ids].sum().item()
                sacred_probs.append(sacred_prob)
            sacred_token_prob = np.mean(sacred_probs) if sacred_probs else 0.0
        else:
            sacred_token_prob = 0.0

        # Compute average token probability
        if hasattr(outputs, 'scores') and outputs.scores:
            avg_probs = []
            for i, step_scores in enumerate(outputs.scores):
                probs = F.softmax(step_scores[0], dim=0)
                if i < len(generated_ids) - 1:
                    token_id = generated_ids[i + 1]
                    avg_probs.append(probs[token_id].item())
            avg_token_prob = np.mean(avg_probs) if avg_probs else 0.0
        else:
            avg_token_prob = 0.0

        # Compute perplexity
        perplexity = np.exp(-np.log(avg_token_prob + 1e-10))

        # BLEU score (if reference available)
        bleu = None
        if "reference_translation" in sent_dict and BLEU_AVAILABLE:
            bleu_result = corpus_bleu([translation], [[sent_dict["reference_translation"]]])
            bleu = bleu_result.score

        metrics = QualityMetrics(
            sacred_token_present=sacred_present,
            sacred_token_probability=sacred_token_prob,
            bleu_score=bleu,
            avg_token_prob=avg_token_prob,
            perplexity=perplexity
        )

        quality_metrics.append(metrics)

    return quality_metrics


