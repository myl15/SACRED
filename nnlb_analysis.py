def plot_universal_circuit_heatmap(
    circuits_by_lang: Dict,
    save_path: Optional[str] = None
):
    """
    Create heatmap showing universal neurons across languages.

    Args:
        circuits_by_lang: Dictionary mapping language codes to circuits
        save_path: Optional path to save figure
    """
    # Get all layers
    all_layers = set()
    for circuit in circuits_by_lang.values():
        all_layers.update(circuit.get_critical_layers())

    layers = sorted(all_layers)
    languages = sorted(circuits_by_lang.keys())

    # Create matrix: layers × languages
    # Value = number of significant neurons in that layer
    matrix = np.zeros((len(layers), len(languages)))

    for lang_idx, lang in enumerate(languages):
        circuit = circuits_by_lang[lang]
        for layer_idx, layer in enumerate(layers):
            layer_neurons = circuit.get_neurons_by_layer(layer)
            matrix[layer_idx, lang_idx] = len(layer_neurons)

    # Plot heatmap
    fig, ax = plt.subplots(figsize=(10, 8))

    im = ax.imshow(matrix, cmap='YlOrRd', aspect='auto')

    # Set ticks
    ax.set_xticks(np.arange(len(languages)))
    ax.set_yticks(np.arange(len(layers)))
    ax.set_xticklabels(languages, rotation=45, ha='right')
    ax.set_yticklabels(layers)

    # Add colorbar
    cbar = plt.colorbar(im, ax=ax)
    cbar.set_label('Number of Significant Neurons', rotation=270, labelpad=20)

    # Add text annotations
    for i in range(len(layers)):
        for j in range(len(languages)):
            text = ax.text(j, i, int(matrix[i, j]),
                          ha="center", va="center", color="black", fontsize=8)

    ax.set_title('Circuit Components Across Languages and Layers', fontweight='bold')
    ax.set_xlabel('Language')
    ax.set_ylabel('Layer')

    plt.tight_layout()

    if save_path:
        Path(save_path).parent.mkdir(parents=True, exist_ok=True)
        plt.savefig(save_path)
        print(f"✓ Universal circuit heatmap saved to {save_path}")
    else:
        plt.show()

    plt.close()


def plot_statistical_summary(
    statistical_report,
    save_path: Optional[str] = None
):
    """
    Visualize statistical test results summary.

    Args:
        statistical_report: StatisticalReport object
        save_path: Optional path to save figure
    """
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))

    # Left plot: Effect sizes
    tests = []
    effect_sizes = []
    colors = []

    if statistical_report.h1_necessity:
        tests.append('H1:\nNecessity')
        effect_sizes.append(statistical_report.h1_necessity.effect_size)
        colors.append('green' if statistical_report.h1_necessity.significant else 'gray')

    if statistical_report.h2_specificity:
        tests.append('H2:\nSpecificity')
        effect_sizes.append(statistical_report.h2_specificity.effect_size)
        colors.append('green' if statistical_report.h2_specificity.significant else 'gray')

    if statistical_report.h3_universality:
        tests.append('H3:\nUniversality')
        effect_sizes.append(statistical_report.h3_universality.effect_size)
        colors.append('green' if statistical_report.h3_universality.significant else 'gray')

    if statistical_report.h4_sufficiency:
        tests.append('H4:\nSufficiency')
        effect_sizes.append(statistical_report.h4_sufficiency.effect_size)
        colors.append('green' if statistical_report.h4_sufficiency.significant else 'gray')

    ax1.barh(tests, effect_sizes, color=colors, alpha=0.7, edgecolor='black')
    ax1.axvline(x=0.5, color='orange', linestyle='--', label='Medium effect')
    ax1.axvline(x=0.8, color='red', linestyle='--', label='Large effect')
    ax1.set_xlabel('Effect Size (Cohen\'s d)')
    ax1.set_title('Hypothesis Test Effect Sizes')
    ax1.legend()
    ax1.grid(axis='x', alpha=0.3)

    # Right plot: P-values
    p_values = []
    p_labels = []

    if statistical_report.h1_necessity:
        p_values.append(statistical_report.h1_necessity.p_value)
        p_labels.append('H1')

    if statistical_report.h2_specificity:
        p_values.append(statistical_report.h2_specificity.p_value)
        p_labels.append('H2')

    if statistical_report.h3_universality:
        p_values.append(statistical_report.h3_universality.p_value)
        p_labels.append('H3')

    # Use log scale for p-values
    log_p_values = [-np.log10(p) for p in p_values]
    colors_p = ['green' if p < 0.01 else 'gray' for p in p_values]

    ax2.barh(p_labels, log_p_values, color=colors_p, alpha=0.7, edgecolor='black')
    ax2.axvline(x=-np.log10(0.05), color='orange', linestyle='--', label='α=0.05')
    ax2.axvline(x=-np.log10(0.01), color='red', linestyle='--', label='α=0.01')
    ax2.set_xlabel('-log₁₀(p-value)')
    ax2.set_title('Statistical Significance')
    ax2.legend()
    ax2.grid(axis='x', alpha=0.3)

    fig.suptitle('Statistical Test Summary', fontsize=14, fontweight='bold')
    plt.tight_layout()

    if save_path:
        Path(save_path).parent.mkdir(parents=True, exist_ok=True)
        plt.savefig(save_path)
        print(f"✓ Statistical summary saved to {save_path}")
    else:
        plt.show()

    plt.close()


