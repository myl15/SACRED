def compute_cohens_d(group1: np.ndarray, group2: np.ndarray) -> float:
    """
    Compute Cohen's d effect size.

    Args:
        group1: First group
        group2: Second group

    Returns:
        Cohen's d value
    """
    mean_diff = np.mean(group1) - np.mean(group2)
    pooled_std = np.sqrt((np.var(group1, ddof=1) + np.var(group2, ddof=1)) / 2)

    if pooled_std == 0:
        return 0.0

    return mean_diff / pooled_std


def bootstrap_confidence_interval(
    data: np.ndarray,
    n_bootstrap: int = 1000,
    alpha: float = 0.05
) -> Tuple[float, float]:
    """
    Compute bootstrap confidence interval.

    Args:
        data: Data array
        n_bootstrap: Number of bootstrap samples
        alpha: Significance level

    Returns:
        Tuple of (lower_bound, upper_bound)
    """
    bootstrap_means = []
    for _ in range(n_bootstrap):
        sample = np.random.choice(data, size=len(data), replace=True)
        bootstrap_means.append(np.mean(sample))

    lower = np.percentile(bootstrap_means, 100 * alpha / 2)
    upper = np.percentile(bootstrap_means, 100 * (1 - alpha / 2))

    return (lower, upper)


def test_h1_necessity(
    baseline_quality: List[float],
    ablated_quality: List[float],
    alpha: float = 0.01
) -> HypothesisTestResult:
    """
    Test H1: Circuit ablation significantly reduces sacred concept translation quality.

    Null hypothesis: No difference in quality after ablation.
    Alternative: Quality decreases after ablation.

    Args:
        baseline_quality: Baseline quality scores
        ablated_quality: Quality scores after ablation
        alpha: Significance threshold

    Returns:
        HypothesisTestResult
    """
    baseline = np.array(baseline_quality)
    ablated = np.array(ablated_quality)

    # Paired t-test (same sentences before/after)
    t_stat, p_value = ttest_rel(baseline, ablated)

    # Effect size
    effect_size = compute_cohens_d(baseline, ablated)

    # Confidence interval
    diff = baseline - ablated
    ci = bootstrap_confidence_interval(diff, n_bootstrap=1000, alpha=alpha)

    # Interpretation
    significant = (p_value < alpha) and (effect_size > 0.5)

    if significant:
        interpretation = (
            f"Circuit is NECESSARY for sacred concept translation. "
            f"Ablation significantly reduces quality (d={effect_size:.3f}, p={p_value:.4f}). "
            f"Effect size is {'large' if abs(effect_size) > 0.8 else 'medium'}."
        )
    else:
        interpretation = (
            f"Circuit necessity NOT confirmed. "
            f"Ablation does not significantly reduce quality (d={effect_size:.3f}, p={p_value:.4f})."
        )

    return HypothesisTestResult(
        hypothesis="H1: Circuit Necessity",
        test_statistic=t_stat,
        p_value=p_value,
        p_value_corrected=None,  # Will be set during correction
        effect_size=effect_size,
        confidence_interval=ci,
        significant=significant,
        interpretation=interpretation
    )


def test_h2_specificity(
    sacred_baseline: List[float],
    sacred_ablated: List[float],
    secular_baseline: List[float],
    secular_ablated: List[float],
    alpha: float = 0.01
) -> HypothesisTestResult:
    """
    Test H2: Circuit ablation affects sacred more than secular sentences.

    Uses 2-way ANOVA to test for interaction effect.

    Args:
        sacred_baseline: Sacred baseline scores
        sacred_ablated: Sacred ablated scores
        secular_baseline: Secular baseline scores
        secular_ablated: Secular ablated scores
        alpha: Significance threshold

    Returns:
        HypothesisTestResult
    """
    # Prepare data for ANOVA
    data = []

    for score in sacred_baseline:
        data.append({'sentence_type': 'sacred', 'intervention': 'baseline', 'quality': score})
    for score in sacred_ablated:
        data.append({'sentence_type': 'sacred', 'intervention': 'ablated', 'quality': score})
    for score in secular_baseline:
        data.append({'sentence_type': 'secular', 'intervention': 'baseline', 'quality': score})
    for score in secular_ablated:
        data.append({'sentence_type': 'secular', 'intervention': 'ablated', 'quality': score})

    df = pd.DataFrame(data)

