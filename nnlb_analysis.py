def plot_circuit_map(
    circuit,
    save_path: Optional[str] = None,
    title: str = "Sacred Concept Circuit Map"
):
    """
    Visualize circuit components across layers.

    Shows neurons by layer with effect sizes.

    Args:
        circuit: Circuit object
        save_path: Optional path to save figure
        title: Plot title
    """
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))

    # Left plot: Neuron count per layer
    neurons_by_layer = {}
    for neuron in circuit.neurons:
        if neuron.layer not in neurons_by_layer:
            neurons_by_layer[neuron.layer] = []
        neurons_by_layer[neuron.layer].append(neuron)

    layers = sorted(neurons_by_layer.keys())
    counts = [len(neurons_by_layer[l]) for l in layers]

    ax1.bar(layers, counts, color='steelblue', alpha=0.7, edgecolor='black')
    ax1.set_xlabel('Layer')
    ax1.set_ylabel('Number of Significant Neurons')
    ax1.set_title('Circuit Neuron Distribution')
    ax1.grid(axis='y', alpha=0.3)

    # Right plot: Effect sizes by layer
    layer_effect_sizes = []
    layer_labels = []

    for layer in layers:
        neurons = neurons_by_layer[layer]
        for neuron in neurons:
            layer_effect_sizes.append(abs(neuron.effect_size))
            layer_labels.append(layer)

    # Violin plot
    df = pd.DataFrame({
        'Layer': layer_labels,
        'Effect Size (|d|)': layer_effect_sizes
    })

    sns.violinplot(
        data=df,
        x='Layer',
        y='Effect Size (|d|)',
        ax=ax2,
        color='coral',
        inner='box'
    )
    ax2.set_title('Effect Size Distribution by Layer')
    ax2.axhline(y=0.5, color='red', linestyle='--', alpha=0.5, label='Medium effect')
    ax2.axhline(y=0.8, color='darkred', linestyle='--', alpha=0.5, label='Large effect')
    ax2.legend()
    ax2.grid(axis='y', alpha=0.3)

    fig.suptitle(title, fontsize=14, fontweight='bold')
    plt.tight_layout()

    if save_path:
        Path(save_path).parent.mkdir(parents=True, exist_ok=True)
        plt.savefig(save_path)
        print(f"✓ Circuit map saved to {save_path}")
    else:
        plt.show()

    plt.close()


def plot_intervention_results(
    necessity_results,
    sufficiency_results=None,
    save_path: Optional[str] = None
):
    """
    Visualize intervention test results.

    Shows before/after comparisons for ablation and restoration.

    Args:
        necessity_results: NecessityResults object
        sufficiency_results: Optional SufficiencyResults object
        save_path: Optional path to save figure
    """
    n_plots = 2 if sufficiency_results is None else 3
    fig, axes = plt.subplots(1, n_plots, figsize=(6 * n_plots, 5))

    if n_plots == 2:
        ax1, ax2 = axes
    else:
        ax1, ax2, ax3 = axes

    # Plot 1: Necessity - Sacred Token Probability
    baseline_probs = [m.sacred_token_probability for m in necessity_results.baseline_quality["sacred"]]
    ablated_probs = [m.sacred_token_probability for m in necessity_results.ablated_quality["sacred"]]

    positions = [1, 2]
    box_data = [baseline_probs, ablated_probs]

    bp1 = ax1.boxplot(box_data, positions=positions, widths=0.6, patch_artist=True,
                      boxprops=dict(facecolor='lightblue', alpha=0.7),
                      medianprops=dict(color='red', linewidth=2))

    ax1.set_xticks(positions)
    ax1.set_xticklabels(['Baseline', 'Ablated'])
    ax1.set_ylabel('Sacred Token Probability')
    ax1.set_title(f'Necessity Test\n(d={necessity_results.effect_size:.3f}, p={necessity_results.p_value:.4f})')
    ax1.grid(axis='y', alpha=0.3)

    # Add significance annotation
    if necessity_results.significant:
        y_max = max(max(baseline_probs), max(ablated_probs))
        ax1.plot([1, 2], [y_max * 1.1, y_max * 1.1], 'k-', linewidth=1)
        ax1.text(1.5, y_max * 1.15, '**', ha='center', fontsize=16)

    # Plot 2: Specificity - Sacred vs Secular
    secular_baseline_probs = [m.sacred_token_probability for m in necessity_results.secular_baseline["secular"]]
    secular_ablated_probs = [m.sacred_token_probability for m in necessity_results.secular_ablated["secular"]]

    x = np.arange(2)
    width = 0.35

    sacred_change = np.mean(baseline_probs) - np.mean(ablated_probs)
    secular_change = np.mean(secular_baseline_probs) - np.mean(secular_ablated_probs)

