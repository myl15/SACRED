    ax2.set_title('Specificity: Sacred vs Secular')
    ax2.set_xticks(x)
    ax2.set_xticklabels(['Sacred\nSentences', 'Secular\nSentences'])
    ax2.legend()
    ax2.grid(axis='y', alpha=0.3)

