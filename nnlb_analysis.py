        restoration_pct = sufficiency_results.restoration_percentage
        ax3.bar([1], [restoration_pct], width=0.6, color='purple', alpha=0.7)
        ax3.axhline(y=0.8, color='red', linestyle='--', label='80% threshold')
        ax3.set_ylim([0, 1.1])
