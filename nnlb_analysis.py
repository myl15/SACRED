class ActivationCapture:
    """
    Proper activation capture with handle management.
    Fixes the hook stacking bug in the original notebook.
    """

    def __init__(self):
        self.handles = []
        self.activations = defaultdict(list)
        self.current_layer = None

    def register_hooks(
        self,
        model,
        layers: List[int],
        component_type: str = "mlp"
    ):
        """
        Register forward hooks on specified layers.

        Args:
            model: NLLB model
            layers: List of layer indices to hook
            component_type: "mlp" for MLP neurons, "attn" for attention heads
        """
        self.cleanup()  # Remove any existing hooks first

        for layer_idx in layers:
            if component_type == "mlp":
                # Hook MLP fc1 (intermediate expansion)
                target_module = model.model.encoder.layers[layer_idx].fc1
            elif component_type == "attn":
                # Hook attention output
                target_module = model.model.encoder.layers[layer_idx].self_attn
            else:
                raise ValueError(f"Unknown component_type: {component_type}")

            handle = target_module.register_forward_hook(
                self._make_hook_fn(layer_idx, component_type)
            )
            self.handles.append(handle)

    def _make_hook_fn(self, layer_idx: int, component_type: str) -> Callable:
        """Create hook function for specific layer and component type."""
        def hook_fn(module, input, output):
            # Store the output with layer information
            if component_type == "mlp":
                # MLP fc1 output: [batch, seq_len, intermediate_dim]
                self.activations[f"mlp_{layer_idx}"].append(
                    output.detach().cpu()
                )
            elif component_type == "attn":
                # Attention output: [batch, seq_len, hidden_dim]
                self.activations[f"attn_{layer_idx}"].append(
                    output[0].detach().cpu() if isinstance(output, tuple) else output.detach().cpu()
                )
        return hook_fn

    def cleanup(self):
        """Remove all registered hooks."""
        for handle in self.handles:
            handle.remove()
        self.handles = []
        self.activations.clear()

    def get_activations(self, layer: int, component_type: str = "mlp") -> torch.Tensor:
        """
        Get captured activations for a specific layer.

        Args:
            layer: Layer index
            component_type: "mlp" or "attn"

        Returns:
            Tensor of activations
        """
        key = f"{component_type}_{layer}"
        if key in self.activations and self.activations[key]:
            return torch.cat(self.activations[key], dim=0)
        return None


def capture_all_activations(
    sentences: List[str],
    model,
    tokenizer,
    lang_code: str,
    layers: Optional[List[int]] = None,
    device: str = "cuda"
) -> Dict[str, Dict[int, torch.Tensor]]:
    """
    Capture MLP and attention activations for all sentences.

    Args:
        sentences: List of sentence texts
        model: NLLB model
        tokenizer: NLLB tokenizer
        lang_code: Language code for tokenization
        layers: List of layers to capture (None = all layers)
        device: Device to run on

    Returns:
        Dictionary mapping component types to layer activations
        {
            "mlp": {0: tensor, 1: tensor, ...},
            "attn": {0: tensor, 1: tensor, ...}
        }
    """
    if layers is None:
        layers = list(range(model.config.encoder_layers))

    all_activations = {"mlp": {}, "attn": {}}

    # Capture MLP activations
    print(f"  Capturing MLP activations for {len(sentences)} sentences...")
    mlp_capture = ActivationCapture()
    mlp_capture.register_hooks(model, layers, component_type="mlp")

    mlp_activations_by_layer = defaultdict(list)
    for sentence in sentences:
        inputs = tokenizer(
            sentence,
            return_tensors="pt",
            src_lang=lang_code
        ).to(device)

        with torch.no_grad():
            # Call encoder directly to skip decoder entirely
            _ = model.model.encoder(
                input_ids=inputs["input_ids"],
                attention_mask=inputs.get("attention_mask")
            )

        # Collect activations from this forward pass
        for layer in layers:
            acts = mlp_capture.get_activations(layer, "mlp")
            if acts is not None:
                # Mean pool across sequence dimension
                pooled = acts.mean(dim=1)  # [batch, intermediate_dim]
                mlp_activations_by_layer[layer].append(pooled)

        mlp_capture.activations.clear()  # Clear for next sentence

    mlp_capture.cleanup()

    # Concatenate all sentences
    for layer in layers:
        if mlp_activations_by_layer[layer]:
            all_activations["mlp"][layer] = torch.cat(
                mlp_activations_by_layer[layer], dim=0
            )

    # Capture attention activations
    print(f"  Capturing attention activations...")
    attn_capture = ActivationCapture()
    attn_capture.register_hooks(model, layers, component_type="attn")

    attn_activations_by_layer = defaultdict(list)
    for sentence in sentences:
        inputs = tokenizer(
            sentence,
            return_tensors="pt",
            src_lang=lang_code
        ).to(device)

        with torch.no_grad():
            # Call encoder directly to skip decoder entirely
            _ = model.model.encoder(
                input_ids=inputs["input_ids"],
                attention_mask=inputs.get("attention_mask")
            )

        for layer in layers:
            acts = attn_capture.get_activations(layer, "attn")
            if acts is not None:
                pooled = acts.mean(dim=1)
                attn_activations_by_layer[layer].append(pooled)

        attn_capture.activations.clear()

    attn_capture.cleanup()

    for layer in layers:
        if attn_activations_by_layer[layer]:
            all_activations["attn"][layer] = torch.cat(
                attn_activations_by_layer[layer], dim=0
            )

    return all_activations


def compute_cohens_d(group1: np.ndarray, group2: np.ndarray) -> float:
    """
    Compute Cohen's d effect size.

    Args:
        group1: First group (e.g., sacred activations)
        group2: Second group (e.g., secular activations)

    Returns:
        Cohen's d value
    """
    mean_diff = np.mean(group1) - np.mean(group2)
    pooled_std = np.sqrt((np.var(group1, ddof=1) + np.var(group2, ddof=1)) / 2)

    if pooled_std == 0:
        return 0.0

    return mean_diff / pooled_std


def permutation_test(
    group1: np.ndarray,
    group2: np.ndarray,
    n_permutations: int = 100
) -> float:
    """
    Non-parametric permutation test for difference in means.

    Args:
        group1: First group
        group2: Second group
        n_permutations: Number of permutations

    Returns:
        p-value
    """
    observed_diff = np.mean(group1) - np.mean(group2)
    combined = np.concatenate([group1, group2])
    n1 = len(group1)

    perm_diffs = []
    for _ in range(n_permutations):
        np.random.shuffle(combined)
        perm_group1 = combined[:n1]
        perm_group2 = combined[n1:]
        perm_diffs.append(np.mean(perm_group1) - np.mean(perm_group2))

    p_value = np.mean(np.abs(perm_diffs) >= np.abs(observed_diff))
    return p_value


def bootstrap_confidence_interval(
    data: np.ndarray,
    n_bootstrap: int = 100,
    alpha: float = 0.05
) -> Tuple[float, float]:
    """
    Compute bootstrap confidence interval for the mean.

    Args:
        data: Data array
        n_bootstrap: Number of bootstrap samples
        alpha: Significance level (default 0.05 for 95% CI)

    Returns:
        Tuple of (lower_bound, upper_bound)
    """
    bootstrap_means = []
    for _ in range(n_bootstrap):
        sample = np.random.choice(data, size=len(data), replace=True)
        bootstrap_means.append(np.mean(sample))

    lower = np.percentile(bootstrap_means, 100 * alpha / 2)
    upper = np.percentile(bootstrap_means, 100 * (1 - alpha / 2))

    return (lower, upper)


def compute_contrastive_scores_with_stats(
    sacred_acts: torch.Tensor,
    secular_acts: torch.Tensor,
    inanimate_acts: torch.Tensor,
    alpha: float = 0.01,
    min_effect_size: float = 0.5
) -> List[Dict[str, Any]]:
    """
    Compute contrastive scores with statistical validation.

    For each neuron:
    - Compute Cohen's d (effect size)
    - Run permutation test (p-value)
    - Compute bootstrap CI
    - Apply FDR correction

    Args:
        sacred_acts: Sacred activations [n_samples, n_neurons]
        secular_acts: Secular activations [n_samples, n_neurons]
        inanimate_acts: Inanimate activations [n_samples, n_neurons]
        alpha: Significance threshold
        min_effect_size: Minimum Cohen's d to consider

    Returns:
        List of significant neurons with statistics
    """
    sacred_np = sacred_acts.cpu().numpy()
    secular_np = secular_acts.cpu().numpy()
    inanimate_np = inanimate_acts.cpu().numpy()

    n_neurons = sacred_np.shape[1]
    neuron_stats = []

    print(f"    Computing statistics for {n_neurons} neurons...")

    from tqdm import tqdm
    for neuron_idx in tqdm(range(n_neurons), desc="    Neurons", leave=False):
        sacred_neuron = sacred_np[:, neuron_idx]
        secular_neuron = secular_np[:, neuron_idx]

        # Compute effect size
        cohens_d = compute_cohens_d(sacred_neuron, secular_neuron)

        # Only proceed if effect size is meaningful
        if abs(cohens_d) < min_effect_size:
            continue

        # Compute p-value (reduced iterations for speed)
        p_value = permutation_test(sacred_neuron, secular_neuron, n_permutations=100)

        # Compute confidence interval (reduced iterations for speed)
        contrast = sacred_neuron - secular_neuron
        ci = bootstrap_confidence_interval(contrast, n_bootstrap=100)

        neuron_stats.append({
            "neuron_idx": neuron_idx,
            "cohens_d": cohens_d,
            "p_value": p_value,
            "ci_lower": ci[0],
            "ci_upper": ci[1],
            "mean_sacred": float(np.mean(sacred_neuron)),
            "mean_secular": float(np.mean(secular_neuron))
        })

    # Apply FDR correction (Benjamini-Hochberg)
    if neuron_stats:
        p_values = [n["p_value"] for n in neuron_stats]
        from statsmodels.stats.multitest import multipletests
        _, corrected_p, _, _ = multipletests(p_values, method='fdr_bh')

        for i, neuron_stat in enumerate(neuron_stats):
            neuron_stat["p_value_corrected"] = corrected_p[i]

        # Filter to significant neurons
        significant = [
            n for n in neuron_stats
            if n["p_value_corrected"] < alpha and abs(n["cohens_d"]) >= min_effect_size
        ]

        print(f"    Found {len(significant)} significant neurons (p < {alpha}, |d| > {min_effect_size})")
        return significant

    return []


