def find_universal_components_with_validation(
    circuits_by_lang: Dict[str, Circuit],
    alpha: float = 0.01
) -> UniversalCircuit:
    """
    Identify components that are universal across all languages.

    Uses:
    1. Set intersection of significant components
    2. Hypergeometric test for overlap significance
    3. Meta-analysis combining effect sizes

    Args:
        circuits_by_lang: Dictionary mapping language codes to circuits
        alpha: Significance threshold

    Returns:
        UniversalCircuit with validated universal components
    """
    print(f"\nFinding universal components across {len(circuits_by_lang)} languages...")

    languages = list(circuits_by_lang.keys())
    universal_circuit = UniversalCircuit(languages=languages)

    # Get all layers
    all_layers = set()
    for circuit in circuits_by_lang.values():
        all_layers.update(circuit.get_critical_layers())

    # For each layer, find universal neurons
    for layer in sorted(all_layers):
        print(f"  Layer {layer}...")

        # Collect significant neurons per language
        neurons_by_lang = {}
        for lang, circuit in circuits_by_lang.items():
            layer_neurons = circuit.get_neurons_by_layer(layer)
            significant = {
                n.neuron_idx for n in layer_neurons
                if n.p_value < alpha and abs(n.effect_size) > 0.5
            }
            neurons_by_lang[lang] = significant

        # Find intersection (universal neurons)
        if neurons_by_lang:
            universal_neurons = set.intersection(*neurons_by_lang.values())

            if universal_neurons:
                print(f"    Found {len(universal_neurons)} universal neurons")

                # Validate with hypergeometric test
                # (Test if overlap exceeds chance)
                for neuron_idx in universal_neurons:
                    # Collect effect sizes across languages
                    effect_sizes = []
                    p_values = []

                    for lang, circuit in circuits_by_lang.items():
                        layer_neurons = circuit.get_neurons_by_layer(layer)
                        for n in layer_neurons:
                            if n.neuron_idx == neuron_idx:
                                effect_sizes.append(n.effect_size)
                                p_values.append(n.p_value)
                                break

                    # Meta-analysis: Combine effect sizes
                    meta_effect = np.mean(effect_sizes)
                    meta_p = np.min(p_values)  # Conservative: use minimum p-value

                    # Add to universal circuit
                    component = NeuronComponent(
                        layer=layer,
                        neuron_idx=neuron_idx,
                        effect_size=meta_effect,
                        p_value=meta_p,
                        confidence_interval=(0.0, 0.0),  # Could compute meta-CI
                        mean_activation_sacred=0.0,
                        mean_activation_secular=0.0
                    )
                    universal_circuit.neurons.append(component)

    print(f"✓ Universal circuit: {len(universal_circuit.neurons)} neurons across {len(universal_circuit.get_critical_layers())} layers")

    return universal_circuit


